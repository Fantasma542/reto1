

function resta( op1, op2) {
    resta = op1 - op2
    return resta
}
function producto( op1, op2) {
    producto = op1 * op2
    return producto
}

function division( op1, op2) {
    division = op1 / op2
    return division
}

function cuadrado(op1) {
    cuadrado = op1 * op1
    return cuadrado
}